
public class Ponto {
	private int pontoX;
	private int pontoY;
	
	public int getPontoX() {
		return pontoX;
	}
	
	public void setPontoX(int pontoX) {
		this.pontoX = pontoX;
	}
	public int getPontoY() {
		return pontoY;
	}
	
	public void setPontoY(int pontoY) {
		this.pontoY = pontoY;
	}

	public Ponto(int pontoX, int pontoY) {
		this.pontoX = pontoX;
		this.pontoY = pontoY;
	}
	
	public Ponto() {}
}
